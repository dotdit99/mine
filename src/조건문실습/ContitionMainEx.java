package 조건문실습;

import java.util.Scanner;

// 시간과 분을 입력받아 45분 일찍 설정되도록 구현
// 23 45 -> 23 00
// 0 30 -> 23 45
public class ContitionMainEx {
    public static void main(String[] args) {
        // 스캐너 객체 생성: 콘솔에서 입력한 데이터를 버퍼(임시 저장소)에 담아둡니다.
        Scanner sc = new Scanner(System.in);

        // 시간을 입력받음: nextInt()는 공백이나 엔터를 기준으로 숫자만 쏙 빼갑니다.
        // 만약 '23 40'을 한꺼번에 치면 23만 먼저 가져가고 40은 버퍼에 남겨둡니다.
        System.out.print("시간을 입력하세요: ");
        int hour = sc.nextInt();

        // 분을 입력받음: 버퍼에 남은 숫자가 있다면 새로 입력을 기다리지 않고 즉시 가져옵니다.
        System.out.print("분을 입력하세요: ");
        int min = sc.nextInt();

        // 계산 편의를 위해 전부 '분' 단위로 환산: 단위 통일은 복잡한 시간 계산의 핵심 전략입니다.
        int alarm = (hour * 60) + min;

        // 45분 전을 구할 때 음수가 나오는 것을 방지하기 위해 미리 하루치(1440분)를 더해줌
        // 24시간 * 60분 = 1440분이며, 0시 30분 같은 상황을 처리하기 위한 필수 예외 처리입니다.
        if (alarm < 45)
            alarm = 24 * 60 + min;

        // 복합 대입 연산자(-=)를 사용하여 45를 뺀 결과를 다시 변수 time에 저장함
        // 만약 '='를 빼고 'time - 45'라고만 쓰면 결과가 저장되지 않아 문법 에러가 발생합니다.
        alarm -= 45;

        // printf의 서식 지정자와 연산자를 이용한 최종 결과 출력
        // 1. %02d: '제로 패딩(Zero Padding)'으로, 0분이나 5분처럼 한 자리 숫자일 때 앞에 0을 붙여 자리를 채워줍니다.
        // 2. time / 60: 전체 분을 60으로 나눈 '몫'을 구해 '시'를 계산합니다.
        // 3. time % 60: 전체 분을 60으로 나눈 '나머지'를 구해 '분'을 계산합니다. (정수 연산이므로 소수점은 자동 버림)
        // 4. printf 안에서는 '+'가 아닌 ','(쉼표)로 서식 문자와 데이터를 연결합니다.
        System.out.printf("알람을 설정한 시간은 %02d시 %02d분 입니다.\n", alarm / 60, alarm % 60);

        sc.close(); // 사용이 끝난 스캐너 자원을 닫아주는 좋은 습관입니다.
    }
}